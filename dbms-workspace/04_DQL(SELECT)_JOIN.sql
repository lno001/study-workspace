SELECT
	A.ANIMAL_NAME,
	A.WEIGHT_KG || 'Kg',
	S.SPECIES_NAME,
	Z.ZONE_NAME,
	K.KEEPER_NAME
FROM
	ANIMAL A
JOIN SPECIES S ON
	(A.SPECIES_ID = S.SPECIES_ID)
JOIN ZONE Z ON
	(Z.ZONE_ID = A.ZONE_ID)
JOIN KEEPER K ON
	(K.KEEPER_ID = A.KEEPER_ID)
ORDER BY A.WEIGHT_KG DESC;
	
