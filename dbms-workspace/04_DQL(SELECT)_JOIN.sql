SELECT
	A.ANIMAL_NAME,
	S.SPECIES_NAME,
	S.SPECIES_CLASS,
	Z.ZONE_NAME,
	Z.ZONE_TYPE,
	NVL(K.KEEPER_NAME, '미배정') AS "KEEPER_NAME"
FROM
	ANIMAL A
JOIN SPECIES S ON
	(A.SPECIES_ID = S.SPECIES_ID)
JOIN ZONE Z ON
	(A.ZONE_ID = Z.ZONE_ID)
LEFT JOIN KEEPER K ON
	(A.KEEPER_ID = K.KEEPER_ID)
ORDER BY 
	A.ANIMAL_ID;





















	
	

