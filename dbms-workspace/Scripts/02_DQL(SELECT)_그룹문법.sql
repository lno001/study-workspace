SELECT
	ANIMAL_NAME,
	LENGTH(ANIMAL_NAME)
FROM
	ANIMAL
WHERE
	LENGTH(ANIMAL_NAME) = 2;

SELECT INSTR('AABAACAABBAA', 'B') FROM DUAL;
SELECT INSTR('AABAACAABBAA', 'B', 1) FROM DUAL;
SELECT INSTR('AABAACAABBAA', 'B', -1) FROM DUAL;
SELECT INSTR('AABAACAABBAA', 'B', -1, 2) FROM DUAL;

SELECT * FROM KEEPER;
SELECT KEEPER_NAME, SUBSTR(KEEPER_NAME, 1, 1) AS "이름 첫글자" FROM KEEPER;

SELECT
	ANIMAL_NAME,
	LPAD(ANIMAL_NAME, 10, '*'),
	RPAD(ANIMAL_NAME, 10, '*')
FROM
	ANIMAL;

SELECT
	KEEPER_NAME,
	RPAD(SUBSTR(KEEPER_NAME, 1, 1), LENGTH(KEEPER_NAME) + 1, '*')
FROM
	KEEPER;

SELECT
	TRIM('                동물원                       ')
FROM
	DUAL;

SELECT
	ZONE_NAME,
	REPLACE(ZONE_NAME, '관', '파크')
FROM
	ZONE;

SELECT
	CONCAT(ANIMAL_NAME, '은(는) 귀엽다')
FROM
	ANIMAL;

SELECT
	ANIMAL_NAME,
	ARRIVAL_DATE,
	FLOOR((MONTHS_BETWEEN(SYSDATE, ARRIVAL_DATE))) AS "개월 수",
	FLOOR((MONTHS_BETWEEN(SYSDATE, ARRIVAL_DATE)) /12) AS "나이"
FROM
	ANIMAL;

SELECT
	ANIMAL_NAME,
	NVL2(KEEPER_ID, '배정완료', '미배정') "사육사 배정여부"
FROM
	ANIMAL;

SELECT
	ANIMAL_NAME,
	GENDER,
	DECODE(GENDER, 'F', '암컷', '수컷') AS "GENDER"
FROM
	ANIMAL;
	
SELECT
	ANIMAL_NAME,
	WEIGHT_KG,
	CASE
		WHEN WEIGHT_KG >= 1000 THEN '초대형'
		WHEN WEIGHT_KG >= 100  THEN '대형'
		WHEN WEIGHT_KG >= 10   THEN '중형'
		ELSE '소형'
	END AS "체급"
FROM
	ANIMAL
ORDER BY WEIGHT_KG DESC;
	
