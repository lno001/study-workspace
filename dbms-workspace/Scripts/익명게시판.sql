CREATE TABLE ANNO_COMMENT(
	COMMENT_NO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
	COMMENT_CONTENT NVARCHAR2(200) NOT NULL,
	USER_ID VARCHAR2(30) NOT NULL,
	USER_PWD VARCHAR2(100) NOT NULL,
	BOARD_NO NUMBER NOT NULL REFERENCES ANNO_BOARD ON DELETE CASCADE,
	DEL_YN CHAR(1) DEFAULT 'N' CHECK(DEL_YN IN('Y', 'N')) NOT NULL,
	REG_DATE TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
)

