SELECT
	A.ANIMAL_NAME,
	S.SPECIES_NAME,
	Z.ZONE_NAME
FROM
	ANIMAL A
JOIN SPECIES S ON
	(
		A.SPECIES_ID = S.SPECIES_ID
	)
JOIN ZONE Z ON
	(
		A.ZONE_ID = Z.ZONE_ID
	);

SELECT
	ANIMAL_NAME,
	(
		SELECT
			SPECIES_NAME
		FROM
			SPECIES S
		WHERE
			A.SPECIES_ID = S.SPECIES_ID
	),
	(
		SELECT
			ZONE_NAME
		FROM
			ZONE Z
		WHERE
			A.ZONE_ID = Z.ZONE_ID
	)
FROM
	ANIMAL A
	
	
	
	
