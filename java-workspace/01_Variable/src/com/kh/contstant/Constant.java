package com.kh.contstant;

public class Constant {
	// * 상수
	// 고정된 수치, 변하지 않는 값
	public static void main(String[] args) {
		// 정수(+,-)
		System.out.println(7);
		System.out.println(-7);
		System.out.println("~~~~~~~");
		// 실수(.)이 있는 숫자
		System.out.println(7.7);
		System.out.println(-7.7);
		System.out.println("~~~~~~~");
		// 자바는 문자와 문자열로 구분
		System.out.println('돈');
		System.out.println("돈 100억");
		// 논리값(true, false)
		System.out.println(true);
		System.out.println(false);
		
		System.out.println("~~~~~~~");
		System.out.println(7);				// 7
		System.out.println('7');			// 칠
		System.out.println("7");			//
		
		
	}
	
}
